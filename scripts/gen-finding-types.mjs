#!/usr/bin/env node
/**
 * Finding-type catalog generator.
 *
 * Scans `src/` for emitted finding `type:` literals and prints a markdown catalog
 * grouped by analyzer module. Keeps the docs (REFERENCE.md "Finding Type Catalog")
 * honest — run `node scripts/gen-finding-types.mjs` and paste the output back in
 * whenever finding types change. Addresses the 2026-06-12 audit gap (most emitted
 * finding types were undocumented).
 *
 * Non-finding `type:` keys are excluded: MCP tool/content schemas, Slack Block Kit,
 * Zod config schema, flow-assertion kinds, and Express response shapes.
 */
import { readdirSync, readFileSync, statSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const SRC = path.join(ROOT, 'src');

// Files whose `type:` keys are structural, not findings.
const EXCLUDE_FILES = new Set([
  'mcp-server.js', 'slack-notifier.js', 'targets.js',
  'slash-command-handler.js', 'interaction-handler.js',
]);
// Literal `type:` values that are structure/protocol, not finding categories.
const EXCLUDE_TYPES = new Set([
  'object', 'number', 'string', 'boolean', 'text',
  'header', 'section', 'mrkdwn', 'plain_text', 'context', 'divider', 'image', 'actions', 'button',
  'ephemeral', 'in_channel', 'tag',
  'no_console_errors', 'url_contains', 'element_visible', 'no_network_errors', // flow-assertion kinds
]);

function walk(dir) {
  let out = [];
  for (const e of readdirSync(dir)) {
    const p = path.join(dir, e);
    if (statSync(p).isDirectory()) out = out.concat(walk(p));
    else if (e.endsWith('.js')) out.push(p);
  }
  return out;
}

const re = /type:\s*['"]([a-z][a-z0-9_]+)['"]/g;
const byFile = {};
const allTypes = new Set();
for (const file of walk(SRC)) {
  const base = path.basename(file);
  if (EXCLUDE_FILES.has(base)) continue;
  const txt = readFileSync(file, 'utf8');
  const set = new Set();
  let m;
  while ((m = re.exec(txt))) {
    const t = m[1];
    if (EXCLUDE_TYPES.has(t)) continue;
    set.add(t); allTypes.add(t);
  }
  if (set.size) byFile[path.relative(SRC, file).replace(/\\/g, '/')] = [...set].sort();
}

const out = [];
out.push('<!-- AUTO-GENERATED by scripts/gen-finding-types.mjs — run it to refresh; do not hand-edit. -->');
out.push('');
out.push(`**${allTypes.size} finding types** emitted across **${Object.keys(byFile).length} modules** (severities are context-dependent — many types emit critical/warning/info by route or magnitude). \`*_summary\` types are always-emitted aggregate rows.`);
out.push('');
out.push('| Module | Finding types |');
out.push('| --- | --- |');
for (const file of Object.keys(byFile).sort()) {
  out.push(`| \`${file}\` | ${byFile[file].map(t => `\`${t}\``).join(', ')} |`);
}
console.log(out.join('\n'));
